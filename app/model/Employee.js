function Employee(id, name, email, phonenumber, birthday, salary, number, position, allowance){
    this._Id = id;
    this._Name = name;
    this._Email = email;
    this._PhoneNumber = phonenumber;
    this._Birthday = birthday;
    this._Salary = salary;
    this._NumberWorkingDay = number;
    this._Position = position;
    this._Allowance = allowance;
}
